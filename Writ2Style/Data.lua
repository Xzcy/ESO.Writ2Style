--Mix data with patch info
function WTS.DataMix()
  for Link, Table in pairs(WTS.Patch) do
    WTS.Data[Table[1]][Table[2]] = Link
  end
end
--/script WritToStyle.DataCheck()
--Check DataBase
function WTS.DataCheck()
  local Data = WTS.Data
  if Data.Version ~= GetESOVersionString() then d("W2S: Need to update") end
  for StyleId, Table in pairs(Data) do
    if not Table or type(Table) ~= "table" then
      d(StyleId)
    else
      if #Table ~= 15 and #Table ~= 1 then
        d(StyleId)
        d(Table)
      end
    end
  end
  d("W2S: Done!")
end
